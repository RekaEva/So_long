#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*tmp;

	tmp = *lst;
	while (tmp)
	{
		del(tmp->content);
		*lst = tmp->next;
		free(tmp);
		tmp = *lst;
	}
	lst = 0;
}
